    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        Airliner addairliner = null;
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String reenterpassword = txtReenterPassword.getText();
        String country = txtCountry.getText();
        if ("".equals(username)) {
            JOptionPane.showMessageDialog(null, "Username cannot be null");
            return;
        }
        if ("".equals(country)) {
            JOptionPane.showMessageDialog(null, "Country cannot be null");
            return;
        }
        if ("".equals(password)) {
            JOptionPane.showMessageDialog(null, "Password cannot be null");
            return;
        }
        if (password == null ? reenterpassword != null : !password.equals(reenterpassword)) {
            JOptionPane.showMessageDialog(null, "The re-enter password is not same as the password");
            return;
        }
        addairliner = new Airliner(country, null, username, password);
        airlinerDirectory.addAirliner(addairliner);
        JOptionPane.showMessageDialog(null, "Create airliner successfully");
    }//GEN-LAST:event_btnRegisterActionPerformed

