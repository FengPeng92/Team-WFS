    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        Airliner loginAirliner = null;
        
        if (username.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in the information", "warning",JOptionPane.WARNING_MESSAGE);  
        } else {
            loginAirliner = airlinerDirectory.searchAirlinerByName(username);
            
            if (loginAirliner != null) {
                if (!loginAirliner.getPassword().equals(password)) {
                    JOptionPane.showMessageDialog(null, "Password is not correct", "warning",JOptionPane.WARNING_MESSAGE); 
                    return;
                }
                System.out.println("Login Success");
                //To be done
                AirlinersJPanel panel = new AirlinersJPanel(UserProcessContainer, airlinerDirectory, loginAirliner);
                UserProcessContainer.add("AirlinersJPanel", panel);
                CardLayout layout = (CardLayout)UserProcessContainer.getLayout();
                layout.next(UserProcessContainer);
                
            } else {
                JOptionPane.showMessageDialog(null, "Username or Password is not correct", "warning",JOptionPane.WARNING_MESSAGE);  
            }
            
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

