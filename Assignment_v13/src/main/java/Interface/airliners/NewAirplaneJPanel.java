    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        try {
            String manufaturer = txtManufaturer.getText();
            String model = txtModel.getText();
            int serialNumber = Integer.parseInt(txtSerialNumber.getText());
            System.out.println("check1");
            if (manufaturer.equals("") || model.equals("")) {
                JOptionPane.showMessageDialog(null, "please fill in all information", "warning",JOptionPane.WARNING_MESSAGE);
            } else {
                System.out.println("check2");
                Airplane newAirplane = new Airplane(loginairliner, serialNumber, model, manufaturer);
                System.out.println("check3");
                loginairliner.addAirplaneIntoFleet(newAirplane);
                JOptionPane.showMessageDialog(null, "Create new airplane sucessfully");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "serial number should be int", "warning",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnCreateActionPerformed

