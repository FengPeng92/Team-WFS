    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String p1 = txtPassword.getText();
        String p2 = txtReenterPassword.getText();
       
         String Name= txtUsername.getText();
        if(Name == null || Name.equals("") ){
            JOptionPane.showMessageDialog(null,"User Name cant be empty", "warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
                
        if(!(txtPassword.getText().equals(txtReenterPassword.getText()))){
            JOptionPane.showConfirmDialog(null,"Password and Re entered password does not match", "warning",JOptionPane.WARNING_MESSAGE);        
            return;
        }
        
        //CustomerDirectory customerDirectory, String userName, String password
        TravelOffice newTravelOffice = new TravelOffice(new CustomerDirectory(), Name, p1);
        travelOfficeDirectory.addNewTravelOffice(newTravelOffice);
        JOptionPane.showMessageDialog(null,"Travel Office Registered Successfully");
        
    }//GEN-LAST:event_btnRegisterActionPerformed

