    private void btnCreateFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateFlightActionPerformed
        // TODO add your handling code here:
        
        String from = txtFrom.getText();
        String to = txtTo.getText();
        String departureTime = txtDepartureTime.getText();
        String arriveTime = txtArriveTime.getText();
        String flightNumber = txtFlightNumber.getText();
        double price = Double.parseDouble(txtPrice.getText());
        
        if (from.equals("") || to.equals("") || departureTime.equals("") || arriveTime.equals("") || flightNumber.equals("") || price == 0) {
            JOptionPane.showMessageDialog(null, "please fill in all information", "warning",JOptionPane.WARNING_MESSAGE); 
        } else {
            //String flightNumber, String from, String to, Date departureTime, Date arriveTime, double price
            try {
                Date date1 = ft.parse(departureTime);
                Date date2 = ft.parse(arriveTime);
                Flight newFlight = new Flight(flightNumber, from, to, date1, date2, price);
                curAir.addFlight(newFlight);
                JOptionPane.showMessageDialog(null, "New flight created successfully!");
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, "Date format is not correct", "warning",JOptionPane.WARNING_MESSAGE); 
            }
            
        }
    }//GEN-LAST:event_btnCreateFlightActionPerformed

