    public void populateTable() {
        DefaultTableModel dtm=(DefaultTableModel) tableTickets.getModel();
        List<Seat> seats = flight.getSeats();
        dtm.setRowCount(0);
        
        for (Seat seat : seats) {
            Object[] row = new Object[4];
            row[0] = seat.getRow();
            row[1] = seat.getColumn();
            row[2] = seat.getLocation();
            row[3] = seat.isIsAvailable();
            dtm.addRow(row);
        }
        
    }

