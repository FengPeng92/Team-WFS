    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            String arriveTime = txtArriveTime.getText();
            String departureTime = txtDepartureTime.getText();
            String flightNumber = txtFlightNumber.getText();
            String from = txtFrom.getText();
            double price = Double.parseDouble(txtPrice.getText());
            String to = txtTo.getText();
            //String flightNumber, String from, String to, Date departureTime, Date arriveTime, double price
            flight.setArriveTime(ft.parse(arriveTime));
            flight.setDepartureTime(ft.parse(departureTime));
            flight.setFlightNumber(flightNumber);
            flight.setFrom(from);
            flight.setTo(to);
            flight.setPrice(price);
            
            txtArriveTime.setEnabled(false);
            txtDepartureTime.setEnabled(false);
            txtFlightNumber.setEnabled(false);
            txtFrom.setEnabled(false);
            txtPrice.setEnabled(false);
            txtTo.setEnabled(false);    
   
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "information format is not correct", "warning",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed


